package com.y5neko.shiroexp.object;

/**
 * ShiroKey类，用以完成key相关操作
 */
public class KeyInfoObj {
    String key;
    String type;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
